    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            
            String username = jTextField1.getText();
            String password = jTextField2.getText();
            Connection con=DriverManager.getConnection("jdbc:derby://localhost:1527/abc","abc","abc");
            Statement st= con.createStatement();
            ResultSet rs= st.executeQuery("select * from details where username = '"+username+"'");
            
            if(rs.next()!=false){
            
            if(password.equals(rs.getString("password")))
                    {
                        if("patient".equals(rs.getString("type")))
                          {
                               dispose();
                                new patient_window(username).setVisible(true);
            
                          }
                         else if("doctor".equals(rs.getString("type")))
                         {
                           dispose();
                           new doctor_window(username).setVisible(true);
                                    
                        }
                    }
            else
            {
                JOptionPane.showMessageDialog(this, "Incorrect Password");
            jTextField1.setText("");
            jTextField2.setText("");
            }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Incorrect Username");
            jTextField1.setText("");
            jTextField2.setText("");
            }
        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

